public class WhileDoWhileLoopDemo {
    public static void main(String[] args) {
        // DO WHILE STATEMENT
        int count = 1;

        do {
            System.out.println("count's value is:" + count);
            count++;
        } while (count <= 10);

        // WHILE STATEMENT
        int count1 = 1;

        while (count1 < 10) {
            System.out.println("count1: " + count1);
            count1++;
        }
    }
}
